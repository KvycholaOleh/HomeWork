package oop1;

public class MyMath {
	
	static double a;
	static double b;
	
	static double addition(double a, double b){
		return a+b;
	}
	
	static double subtraction(double a, double b){
		return a-b;
	}
	
	static double multiplication(double a, double b){
		return a*b;
	}
	
	static double divide(double a, double b){
		return a/b;
	}

}
